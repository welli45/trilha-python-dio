# declarando variáveis
produto_1 = 20
produto_2 = 10
# somando valores
print(produto_1 + produto_2)
# subtraindo valores
print(produto_1 - produto_2)
# dividindo valores
print(produto_1 / produto_2)
# divisão inteira
print(produto_1 // produto_2)
# multiplicação
print(produto_1 * produto_2)
# módulo
print(produto_1 % produto_2)
# exponenciação
print(produto_1 ** produto_2)

# mudnado a ordem de execução
x = (10 + 5) * 4
y = (10 / 2) + (25 * ((2 - 2) ** 2))
print(x)
print(y)
