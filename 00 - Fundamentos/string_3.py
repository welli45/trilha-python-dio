# fatiamento da string 

# retorna o final e o início da string
nome = "Guilherme Arthur de Carvalho"
# primeiro caractere
print(nome[0])
# pega a penúltima string. 
print(nome[-2])
# para no caractere 9. do caractere 1 até o 9
print(nome[:9])
# aqui é o contrário
print(nome[10:])
# aqui pega o range entre 10 e 16
print(nome[10:16])
# aqui pega do do 10 a 16 de 2 em 2.
print(nome[10:16:2])
# retorna toda string
print(nome[:])
# aqui espelha a string, isto é, pega os valores invertidos. 
print(nome[::-1])
